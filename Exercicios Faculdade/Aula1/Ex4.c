// Determine a área de um triângulo.

#include <stdio.h>
#include <stdlib.h>
#include <locale.h>

int main() {

    float base;
    printf("Digite o valor da base em cm ");
    scanf("%f", &base);

    float altura;
    printf("Digite o valor da altura em cm ");
    scanf("%f", &altura);

    float area = (base * altura) / 2;

    printf("A área total é de %f cm", area);











}